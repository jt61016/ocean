    /**
     * 这段代码的详细分析:
     * http://www.hollischuang.com/archives/1058
     *
     * @param i 需要转换的整数
     * @param index 长度
     * @param buf 输出
     */
    static void getChars(int i, int index, char[] buf) {
        int q, r;
        int charPos = index;
        char sign = 0;

        if (i < 0) {
            sign = '-';
            i = -i;
        }

        // Generate two digits per iteration
        // 运算效率: 移位 > 乘法 > 除法
        while (i >= 65536) {
            q = i / 100;
            // really: r = i - (q * 100);
            r = i - ((q << 6) + (q << 5) + (q << 2));
            i = q;
            buf [--charPos] = DigitOnes[r];
            buf [--charPos] = DigitTens[r];
        }

        // Fall thru to fast mode for smaller numbers
        // assert(i <= 65536, i);
        for (;;) {
            // 约等于 i * 0.1,至于为什么选 52429 和 16+3,是因为考虑精确度以及运算过程中的溢出
            // 右移运算中防止 (i * 52429)溢出 2^32, i需要小于2^16,所以上一个while循环判断条件是i >= 65536
            q = (i * 52429) >>> (16+3);
            r = i - ((q << 3) + (q << 1));
            buf [--charPos] = digits [r];
            i = q;
            if (i == 0) break;
        }
        if (sign != 0) {
            buf [--charPos] = sign;
        }
    }

